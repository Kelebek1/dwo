def char_id_to_name(id):
    if id in CHARS:
        return CHARS[id]
    elif id == -1 or id == 0xFFFFFFFF:
        return "None"
    else:
        return ""

def char_name_to_id(name):
    if name == "":
        print(f"Ambiguous empty name")
        exit()
    if name in CHARS:
        return CHARS[name]
    print(f"Unknown char name {name}")
    exit()
